export default function RegisterPage() {
  const { register: registerUser } = useAuth();
  const [consentChecked, setConsentChecked] = useState(false);
  const [showModal, setShowModal] = useState(false);
  const [termsChecked, setTermsChecked] = useState(false);
  const [showTermsModal, setShowTermsModal] = useState(false);
  const {
    register,
    handleSubmit,
    setError,
    formState: { errors, isSubmitting },
  } = useForm<FormData>({ resolver: zodResolver(schema) });

  const onSubmit = async (data: FormData) => {
    if (!consentChecked || !termsChecked) {
      setError('root', { message: 'Необходимо принять политику обработки персональных данных и пользовательское соглашение' });
      return;
    }
    try {
      await registerUser(
        { email: data.email, full_name: data.full_name, password: data.password },
        true,
      );
    } catch (err) {
      setError('root', {
        message: getApiErrorMessage(err, 'Не удалось зарегистрироваться.'),
      });
    }
  };

  return (
    <>
      {showModal && <ConsentModal onClose={() => setShowModal(false)} />}
      {showTermsModal && <TermsModal onClose={() => setShowTermsModal(false)} />}
      <Title>Регистрация</Title>
      <Sub>Создайте аккаунт для получения консультаций</Sub>
      <Form onSubmit={handleSubmit(onSubmit)}>
        {errors.root && <ServerError>{errors.root.message}</ServerError>}
        <Field>
          <Label>Полное имя</Label>
          <Input
            {...register('full_name')}
            placeholder="Иванов Иван Иванович"
            $error={!!errors.full_name}
            autoComplete="name"
          />
          {errors.full_name && <ErrorText>{errors.full_name.message}</ErrorText>}
        </Field>
        <Field>
          <Label>Email</Label>
          <Input
            {...register('email')}
            type="email"
            placeholder="you@example.com"
            $error={!!errors.email}
            autoComplete="email"
          />
          {errors.email && <ErrorText>{errors.email.message}</ErrorText>}
        </Field>
        <Field>
          <Label>Пароль</Label>
          <Input
            {...register('password')}
            type="password"
            placeholder="Минимум 8 символов, 1 заглавная, 1 цифра"
            $error={!!errors.password}
            autoComplete="new-password"
          />
          {errors.password && <ErrorText>{errors.password.message}</ErrorText>}
        </Field>
        <Field>
          <Label>Подтверждение пароля</Label>
          <Input
            {...register('confirm_password')}
            type="password"
            placeholder="Повторите пароль"
            $error={!!errors.confirm_password}
            autoComplete="new-password"
          />
          {errors.confirm_password && <ErrorText>{errors.confirm_password.message}</ErrorText>}
        </Field>
        <ConsentRow>
          <input
            type="checkbox"
            id="consent"
            checked={consentChecked}
            onChange={(e) => setConsentChecked(e.target.checked)}
          />
          <label htmlFor="consent">
            Я согласен с{' '}
            <ConsentLink type="button" onClick={() => setShowModal(true)}>
              политикой обработки персональных данных
            </ConsentLink>
          </label>
        </ConsentRow>
        <ConsentRow>
          <input
            type="checkbox"
            id="terms"
            checked={termsChecked}
            onChange={(e) => setTermsChecked(e.target.checked)}
          />
          <label htmlFor="terms">
            Я принимаю{' '}
            <ConsentLink type="button" onClick={() => setShowTermsModal(true)}>
              пользовательское соглашение
            </ConsentLink>
          </label>
        </ConsentRow>
        <Btn type="submit" disabled={isSubmitting}>
          {isSubmitting ? 'Создание аккаунта...' : 'Создать аккаунт'}
        </Btn>
      </Form>
      <Footer>
        Уже есть аккаунт? <Link to="/login">Войти</Link>
      </Footer>
    </>
  );
}
